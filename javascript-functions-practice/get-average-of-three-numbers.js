/* exported getAverageOfThreeNumbers */
/**
 * Returns the average of x, y, z.
 * Recall average is found by adding up all the values and then dividing up the sum by the number of values you added together.
 * @param {number} x The first value.
 * @param {number} y The second value.
 * @param {number} z The third value.
 * @returns The sum of x, y, z, divided by 3.
 */
function getAverageOfThreeNumbers(x, y, z) {
  return (x + y + z) / 3;
}
