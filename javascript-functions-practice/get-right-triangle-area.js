/* exported getRightTriangleArea */
/**
 * Recall A = (base * height) / 2
 * @param {number} base a number representing the base of a right triangle
 * @param {number} height a number representing the height of a right triangle
 * @returns The area of a right triangle with a base of base and a height of height.
 */
function getRightTriangleArea(base, height) {
  return (base * height) / 2;
}
